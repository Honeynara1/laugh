package com.app.dto;

public class ApiResponse {

	private String msg;
	
	

	public ApiResponse() {
		super();
	}



	public ApiResponse(String msg) {
		super();
		this.msg = msg;
	}



	public String getMsg() {
		return msg;
	}



	public void setMsg(String msg) {
		this.msg = msg;
	}
	
	
	
	
}
